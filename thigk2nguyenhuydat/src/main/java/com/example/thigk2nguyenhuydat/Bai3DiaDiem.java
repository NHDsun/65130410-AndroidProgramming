package com.example.thigk2nguyenhuydat;

public class Bai3DiaDiem {
    int hinh;
    String ten, diaChi;

    public Bai3DiaDiem(int hinh, String ten, String diaChi) {
        this.hinh = hinh;
        this.ten = ten;
        this.diaChi = diaChi;
    }

    public int getHinh() { return hinh; }
    public String getTen() { return ten; }
    public String getDiaChi() { return diaChi; }
}
