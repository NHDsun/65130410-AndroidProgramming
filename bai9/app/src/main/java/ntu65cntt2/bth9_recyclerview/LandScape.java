package ntu65cntt2.bth9_recyclerview;

public class LandScape {
    String landCaption;
     int landImageId;

    public LandScape(String landCaption, int landImageId) {
        this.landCaption = landCaption;
        this.landImageId = landImageId;
    }

    public String getLandCaption() {
        return landCaption;
    }


    public int getLandImageId() {
        return landImageId;
    }


}
